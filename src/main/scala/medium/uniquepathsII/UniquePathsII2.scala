    case (m, n) =>
      case class Point(x: Int, y: Int) {
        def isFree = obstacleGrid(y)(x) == 0

        def right = if (x + 1 < m) Some(copy(x = x + 1)).filter(_.isFree) else None

        def down = if (y + 1 < n) Some(copy(y = y + 1)).filter(_.isFree) else None
      }
      val start = Point(0, 0)
      val finish = Point(m - 1, n - 1)

      def search(step: Point = start): Int = if (step == finish) 1 else step.right.map(search).getOrElse(0) + step.down.map(search).getOrElse(0)


      if (start.isFree && finish.isFree) search() else 0
  }

